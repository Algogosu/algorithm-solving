public class Main {
    public static void main(String[] args) {
        // Java에서 "는 역슬래시를 붙여서 나타내야함
        System.out.println("         ,r'\"7");
        System.out.println("r`-_   ,'  ,/");    
        // \는 \\로 두 번 붙여야함
        System.out.println(" \\. \". L_r'");
        System.out.println("   `~\\/");
        System.out.println("      |");
        System.out.println("      |");
    }
}